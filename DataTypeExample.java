
public class DataTypeExample {
    public static void main(String[] args){
        byte myByte = 10;
        short myShort = 200;
        int myInt = 300;
        long myLong = 70000000L;

        float myFloat = 7.98f;
        double myDouble = 80.77777;

        String myString ="My name is Arvind kumar";

        boolean myBoolean = true;
        char myChar = 'A';

        System.out.println("print the value of my myByte" +myByte);
        System.out.println("print the value of my myShort" +myShort);
        System.out.println("print the value of my myInt"  + myInt);
        System.out.println("print the value of my myLong" + myLong);
        System.out.println("print the value of my myFloat"+  myFloat);
        System.out.println("print the value of my myDouble"+ myDouble);
        System.out.println("print the value of my myString"+ myString);
        System.out.println("print the value of my myBoolean"+myBoolean);
        System.out.println("print the value of my myChar"   +   myChar);
    }
}
